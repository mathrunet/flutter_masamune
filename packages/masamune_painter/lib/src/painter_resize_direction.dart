/// ResizeDirection enumeration.
///
/// リサイズ方向の列挙型。
enum PainterResizeDirection {
  /// Top left.
  ///
  /// 左上。
  topLeft,

  /// Top right.
  ///
  /// 右上。
  topRight,

  /// Bottom left.
  ///
  /// 左下。
  bottomLeft,

  /// Bottom right.
  ///
  /// 右下。
  bottomRight,

  /// Left.
  ///
  /// 左。
  left,

  /// Right.
  ///
  /// 右。
  right,

  /// Top.
  ///
  /// 上。
  top,

  /// Bottom.
  ///
  /// 下。
  bottom,
}
